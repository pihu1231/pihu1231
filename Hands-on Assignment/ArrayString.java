package core.java;

public class ArrayString {

	public static void main(String[] args) {
//		Initialization  and Declaration of String Array:
		String name[] = { "Aman", "parul", "Mitra", "Akansha" };
//		traverse the Array to print the input of String Array:
		for (int i = 0; i < name.length; i++) {
			System.out.println(name[i]);
		}
	}

}
